# --- Helpers ---
def set_cell_shading(cell, color):
    shading = OxmlElement('w:shd')
    shading.set(qn('w:fill'), color)
    shading.set(qn('w:val'), 'clear')
    cell._tc.get_or_add_tcPr().append(shading)

def add_table(doc, headers, rows, col_widths=None):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.LEFT
    for i, h in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = h
        for p in cell.paragraphs:
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            for r in p.runs:
                r.font.bold = True
                r.font.size = Pt(9)
                r.font.color.rgb = RGBColor(0x1A, 0x36, 0x5D)
        set_cell_shading(cell, 'F0F4F8')
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            cell = table.rows[ri + 1].cells[ci]
            cell.text = str(val)
            for p in cell.paragraphs:
                for r in p.runs:
                    r.font.size = Pt(9)
    if col_widths:
        for i, w in enumerate(col_widths):
            for row in table.rows:
                row.cells[i].width = Cm(w)
    doc.add_paragraph()
    return table

def add_bold_paragraph(doc, bold_text, normal_text=''):
    p = doc.add_paragraph()
    r = p.add_run(bold_text)
    r.bold = True
    if normal_text:
        p.add_run(normal_text)
    return p

def add_box(doc, title, text, title_color=RGBColor(0x74, 0x42, 0x10)):
    p = doc.add_paragraph()
    r = p.add_run(f'{title}: ')
    r.bold = True
    r.font.color.rgb = title_color
    r2 = p.add_run(text)
    r2.font.color.rgb = title_color
    return p

def add_bullet(doc, bold_part, text):
    p = doc.add_paragraph(style='List Bullet')
    if bold_part:
        r = p.add_run(bold_part)
        r.bold = True
    if text:
        p.add_run(text)
    return p


